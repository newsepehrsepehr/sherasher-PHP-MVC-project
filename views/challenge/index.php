<script>
    $('.sidebar-challenge li').click(function () {
        $('.sidebar-challenge li').removeClass('active')
        $(this).addClass('active');
        var liIndex = $(this).attr('data-toggle');
        $('.content-challenge').fadeOut(0);
        $('#challenge'+liIndex).fadeIn(600);
    })

    var navPosition = $('.content-challenge .navbar').offset().top;

    $(window).scroll(function() {

        if ($(this).scrollTop() > navPosition){
            $('.content-challenge .navbar').addClass('scroll');
        }
        else{
            $('.content-challenge .navbar').removeClass('scroll');
        }
    });
</script>